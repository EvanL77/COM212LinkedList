public class Student
{
	private String name;
	private int grade;
	
	public Student(String name0, int grade0)
	{
		name = name0;
		grade = grade0;
	}
	public String getName()
	{
		return name;
	}
	public void setGrade(int grade0)
	{
		grade = grade0;
	}
	public int getGrade()
	{
		return grade;
	}
	
	
}
